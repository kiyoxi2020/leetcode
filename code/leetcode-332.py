class Solution(object):
    def findItinerary(self, tickets):
        """
        :type tickets: List[List[str]]
        :rtype: List[str]
        """
        import collections
        n = len(tickets)
        tickets.sort(key=lambda x: x[1])
        dict0 = collections.defaultdict(list)
        for i, j in tickets:
            dict0[i].append(j)

        lists = ["JFK"]
        def dfs():
            if len(lists) == n+1:
                return True
            n_t = len(dict0[lists[-1]])
            for _ in range(n_t):
                t = dict0[lists[-1]].pop(0)
                lists.append(t)
                if dfs():
                    return True
                lists.pop(-1)
                dict0[lists[-1]].append(t)
            return False

        dfs()
        return lists


