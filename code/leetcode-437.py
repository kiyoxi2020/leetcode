# Definition for a binary tree node.
# class TreeNode(object):
#     def __init__(self, val=0, left=None, right=None):
#         self.val = val
#         self.left = left
#         self.right = right
class Solution(object):
    def pathSum(self, root, targetSum):
        """
        :type root: TreeNode
        :type targetSum: int
        :rtype: int
        """
        def dfs(h, list0):
            if not h: return 0
            v = h.val
            list0 = [i+v for i in list0]+[v]
            return list0.count(targetSum) + dfs(h.left, [i for i in list0]) + dfs(h.right, [i for i in list0])
            
        return dfs(root, [])


            
